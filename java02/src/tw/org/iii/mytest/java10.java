package tw.org.iii.mytest;

public class java10 {
	public static void main(String[] args) {
		int a=11;
		
		//以下為switch...case...用法
		switch(a) {
		case 1:
			System.out.println("A");
			break;
		case 10:
			System.out.println("B");
			break;
		case 100:
			System.out.println("C");
			break;
		default:
			System.out.println("D");
			break;
		}

	}

}
